namespace NovaLab.Server.Data.Shared;

// ---------------------------------------------------------------------------------------------------------------------
// Code
// ---------------------------------------------------------------------------------------------------------------------
public record CreateFromDtoResponse<T> (
    bool IsSuccessful,
    T? Subject
) where T : class {
    public bool IsFailure => !IsSuccessful;
    
    // -----------------------------------------------------------------------------------------------------------------
    // Methods
    // -----------------------------------------------------------------------------------------------------------------
    public static CreateFromDtoResponse<T> Failure() {
        return new CreateFromDtoResponse<T>(false, null);
    }
    public static CreateFromDtoResponse<T> Success(T subject) {
        return new CreateFromDtoResponse<T>(true, subject);
    }
    
}
